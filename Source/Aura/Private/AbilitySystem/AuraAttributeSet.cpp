void UAuraAttributeSet::PreAttributeBaseChange(const FGameplayAttribute& Attribute, float& NewValue) const
{
	Super::PreAttributeBaseChange(Attribute, NewValue);
	
	if (Attribute == GetHealthAttribute())
	{
		UE_LOG(LogTemp,Warning,TEXT("Health %f"), NewValue);
		NewValue = FMath::Clamp(NewValue, 0.0f, GetMaxHealth());
	}
	if (Attribute == GetManaAttribute())
	{
		UE_LOG(LogTemp,Warning,TEXT("Mana %f"), NewValue);
		NewValue = FMath::Clamp(NewValue, 0.0f, GetMaxMana());
	}
}

void UAuraAttributeSet::SetEffectProperties(const FGameplayEffectModCallbackData& Data,
	FEffectProperties& Props) const
{
	// Source = causer
	
	Props.ActiveContextHandle = Data.EffectSpec.GetContext();
	Props.SourceAbilitySystemComponent = Props.ActiveContextHandle.GetOriginalInstigatorAbilitySystemComponent();

	if (IsValid(Props.SourceAbilitySystemComponent) && Props.SourceAbilitySystemComponent->AbilityActorInfo.IsValid()
			&& Props.SourceAbilitySystemComponent->AbilityActorInfo->AvatarActor.IsValid())
	{
		Props.SourceAvatarActor = Props.SourceAbilitySystemComponent->AbilityActorInfo->AvatarActor.Get();
		Props.SourceController = Props.SourceAbilitySystemComponent->AbilityActorInfo->PlayerController.Get();
		if (Props.SourceController == nullptr && Props.SourceAvatarActor != nullptr)
		{
			if (const APawn* Pawn = Cast<APawn>(Props.SourceAvatarActor))
			{
				Props.SourceController = Pawn->GetController();
			}
		}
		if (Props.SourceController)
		{
			Props.SourceCharacter = Cast<ACharacter>(Props.SourceController->GetPawn());
		}
	}
	if (Data.Target.AbilityActorInfo.IsValid() && Data.Target.AbilityActorInfo->AvatarActor.IsValid())
	{
		Props.TargetAvatarActor = Data.Target.AbilityActorInfo->AvatarActor.Get();
		Props.TargetController = Data.Target.AbilityActorInfo->PlayerController.Get();
		Props.TargetCharacter = Cast<ACharacter>(Props.TargetAvatarActor);
		Props.TargetAbilitySystemComponent = UAbilitySystemBlueprintLibrary::GetAbilitySystemComponent(Props.TargetAvatarActor);
	}	
}

void UAuraAttributeSet::PostGameplayEffectExecute(const struct FGameplayEffectModCallbackData& Data)
{
	Super::PostGameplayEffectExecute(Data);

	FEffectProperties EffectProps;
	SetEffectProperties(Data, EffectProps);

	// Clamping Health and Mana
	if (Data.EvaluatedData.Attribute == GetHealthAttribute())
	{
		SetHealth(FMath::Clamp(GetHealth(), 0.0f, GetMaxHealth()));
		UE_LOG(LogTemp, Warning, TEXT("Change Health on %s, New Health: %f"), *GetNameSafe(EffectProps.TargetAvatarActor), GetHealth());
	}
	if (Data.EvaluatedData.Attribute == GetManaAttribute())
	{
		SetMana(FMath::Clamp(GetMana(), 0.0f, GetMaxMana()));
	}
	// Using Meta attributes to change the attributes
	if (Data.EvaluatedData.Attribute == GetInComingDamageAttribute())
	{
		const float LocalInComingDamage = GetInComingDamage();
		SetInComingDamage(0.f);
		if (LocalInComingDamage > 0.f)
		{
			const float NewHealth = GetHealth() - LocalInComingDamage;
			SetHealth(FMath::Clamp(NewHealth, 0.0f, GetMaxHealth()));

			const bool bFatal = NewHealth <= 0.f;
			// Activate Hit React in Generic way (Not Fatal)
			if (bFatal)
			{
				ICombatInterface* CombatInterface = Cast<ICombatInterface>(EffectProps.TargetAvatarActor);
				if (CombatInterface)
				{
					CombatInterface->Die();
				}
			}
			else
			{
				FGameplayTagContainer TagContainer;
				TagContainer.AddTag(FAuraGameplayTags::Get().Effect_HitReact);
				EffectProps.TargetAbilitySystemComponent->TryActivateAbilitiesByTag(TagContainer);
			}
	
			const bool bBlocked = UAuraAbilitySystemLibrary::IsBlockedHit(EffectProps.ActiveContextHandle);
			const bool bCritical = UAuraAbilitySystemLibrary::IsCriticalHit(EffectProps.ActiveContextHandle);
			ShowFloatingDamageText(EffectProps, LocalInComingDamage, bBlocked, bCritical);
		}
	}
}

void UAuraAttributeSet::ShowFloatingDamageText(const FEffectProperties& EffectProps, float Damage, bool bBlockedHit, bool bCriticalHit) const
{
	if (EffectProps.SourceCharacter != EffectProps.TargetCharacter)
	{
		if (AAuraPlayerController* PlayerController = Cast<AAuraPlayerController>(EffectProps.SourceCharacter->Controller))
		{
			PlayerController->ShowDamageNumbers(EffectProps.TargetCharacter, Damage, bBlockedHit, bCriticalHit);
			return;
		}
		if (AAuraPlayerController* PlayerController = Cast<AAuraPlayerController>(EffectProps.TargetCharacter->Controller))
		{
			PlayerController->ShowDamageNumbers(EffectProps.TargetCharacter, Damage, bBlockedHit, bCriticalHit);
		}
	}
}

